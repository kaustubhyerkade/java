
public class Employe {
	private int empId;
	private String empName;
	private double EmpSal;

	
	public Employe(int empId, String empName, double empSal) {
		super();
		this.empId = empId;
		this.empName = empName;
		EmpSal = empSal;
	}

	public int getEmpId() {
		return empId;
	}

	public String getEmpName() {
		return empName;
	}

	public double getEmpSal() {
		return EmpSal;
	}
	
	
	
	
	
	

}
