
public class Address {
	
	
	private String AddrCity;
	private int AddrPin;
	
	public Address(String addrCity, int addrPin) {
		super();
		AddrCity = addrCity;
		AddrPin = addrPin;
	}
	public String getAddrCity() {
		return AddrCity;
	}
	public int getAddrPin() {
		return AddrPin;
	}
	
	
	

}
